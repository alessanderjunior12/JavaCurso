public class Exemplo3 {
	public static void main(String[] args) {

		int contador = 1;
		System.out.println(++contador); //somar e depois imprimir 2
		System.out.println(contador); //2
		
		System.out.println(--contador); //subtrair e depois imprimir 1
		System.out.println(contador--); //imprimir o numero 1 e depois subtrair
		System.out.println(contador); //0
		
		
		
	}
}
