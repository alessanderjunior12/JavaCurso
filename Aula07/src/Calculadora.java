
public class Calculadora {

	public int somar(int a, int b) {
		int resultado = a + b;
		return resultado;
	}
	
	public int somar(int a, int b, int c) {
		int resultado = a + b + c;
		return resultado;
	}

	public float somar(float a, float b) {
		float resultado = a + b;
		return resultado;
	}

	
}
